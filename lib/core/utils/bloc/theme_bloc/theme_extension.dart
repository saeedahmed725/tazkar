import 'package:flutter/material.dart';

extension ThemeExtensionX on ThemeData {
  static ThemeMode fromName(String value) {
    switch (value) {
      case 'light':
        return ThemeMode.light;
      case 'dark':
        return ThemeMode.dark;
      case 'system':
        return ThemeMode.system;
      default:
        return ThemeMode.system;
    }
  }
}